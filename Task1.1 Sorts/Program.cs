using System;

class Program
{
    // Метод для вычисления времени сортировки методом вставок для n элементов
    static double InsertionSortTime(int n)
    {
        // Формула для времени сортировки методом вставок: 8 * n^2
        return 8 * n * n;
    }

    // Метод для вычисления времени сортировки методом слияния для n элементов
    static double MergeSortTime(int n)
    {
        // Формула для времени сортировки методом слияния: 64 * n * log2(n)
        return 64 * n * Math.Log(n, 2);
    }

    static void Main(string[] args)
    {
        // Начальное значение n. Количество элементов, которые требуется отсортировать
        int n = 10;

        // Увеличиваем значение n до тех пор, пока время сортировки методом вставок не превысит время сортировки методом слияния
        while (InsertionSortTime(n) <= MergeSortTime(n))
        {
            n++;
        }

        // Выводим результат, когда время сортировки методом вставок превысит время сортировки методом слияния
        Console.WriteLine($"Время сортировки методом вставок превысит время сортировки методом слияния при n = {n}");
    }
}
